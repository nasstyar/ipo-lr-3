num1 = int(input("Введите первое число: "))#Запрашиваем у пользователя первое число
num2 = int(input("Введите второе число: "))#Запрашиваем у пользователя второе число
if num1<num2:#Созадаем первое условие
    print("Наименьшее число: ", num1)#Выводим наименьшее число
elif num1>num2:#Создаем второе условие
    print("Наименьшее число: ", num2)#Выводим наименьшее число
elif num1==num2:#Создаем третье условие
    print("Числа равны")#Выводим наименьшее число
